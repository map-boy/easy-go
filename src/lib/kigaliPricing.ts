export interface ZonePriceLookupResult {
  priceRwf:    number;
  fromZone:    string;
  toZone:      string;
  distKm:      number;
  confidence:  'exact' | 'near' | 'estimated';
  source:      'table' | 'formula';
  multipliers: string[];
}

// ── MAIN PRICE LOOKUP ─────────────────────────────────────────────────────────
// Looks up price from zone table. Map/GPS only used for km display, never for price.
export function lookupPrice(input: OfflinePriceInput): ZonePriceLookupResult | null {
  const fromMatch = findZone(input.senderLocation);
  const toMatch   = findZone(input.receiverLocation);
  if (!fromMatch || !toMatch) return null;

  const { zone: from } = fromMatch;
  const { zone: to }   = toMatch;

  // Sort IDs so lookup key is always consistent
  const [idA, idB] = [from.id, to.id].sort();
  const key = `${idA}:${idB}`;

  // Same zone — minimum price
  if (from.id === to.id) {
    return {
      priceRwf:   MIN_PRICE,
      fromZone:   from.name,
      toZone:     to.name,
      distKm:     0.8,
      confidence: 'exact',
      source:     'table',
      multipliers: [],
    };
  }

  // Get base price from table (or fall back to formula)
  const tablePrice = ZONE_PRICE_TABLE[key];
  const straightKm = haversine(from.lat, from.lng, to.lat, to.lng);
  const distKm     = Math.round(straightKm * ROAD_FACTOR * 10) / 10;
  let   basePrice  = tablePrice ?? (BASE_FIXED + distKm * BASE_RATE_PER_KM);
  const source: 'table' | 'formula' = tablePrice ? 'table' : 'formula';

  // Apply condition multipliers
  const multipliers: string[] = [];
  if (input.isRushHour)              { basePrice *= 1.20; multipliers.push('🕐 Rush +20%');      }
  if (input.isRaining)               { basePrice *= 1.15; multipliers.push('🌧️ Rain +15%');      }
  if (input.poorRoads)               { basePrice *= 1.10; multipliers.push('🪨 Roads +10%');     }
  if (input.packageSize === 'large') { basePrice *= 1.30; multipliers.push('📦 Large +30%');     }
  if (input.packageSize === 'small') { basePrice *= 0.85; multipliers.push('📦 Small −15%');     }
  if (input.isRapid)                 { basePrice *= 1.20; multipliers.push('⚡ Rapid +20%');      }

  const rank = { exact: 3, near: 2, estimated: 1 };
  const confidence = rank[fromMatch.confidence] <= rank[toMatch.confidence]
    ? fromMatch.confidence : toMatch.confidence;

  return {
    priceRwf:   Math.max(Math.round(basePrice / 100) * 100, MIN_PRICE),
    fromZone:   from.name,
    toZone:     to.name,
    distKm,
    confidence,
    source,
    multipliers,
  };
}